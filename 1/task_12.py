# Табулирование функции y = x^2 - 2x + 1
def func(x):
    return x**2 - 2*x + 1

# Ввод интервала и шага
try:
    A = float(input("Введите начало интервала A: "))
    B = float(input("Введите конец интервала B: "))
    H = float(input("Введите шаг H: "))
except ValueError:
    print("Некорректный ввод. Введите числа.")
    exit(1)

# Проверка шага
if H <= 0:
    print("Шаг должен быть положительным.")
    exit(1)

# Заголовок таблицы
print("\n{:>10} | {:>15}".format("x", "y = x^2 - 2x + 1"))
print("-" * 28)

# Табулирование
x = A
while x <= B + 1e-8: # Добавляем небольшой запас для B
    y = func(x)
    print("{:10.4f} | {:15.4f}".format(x, y))
    x += H